    private void BouttonFichierArffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BouttonFichierArffActionPerformed
   
            CalculTFIDF cltfidf = new CalculTFIDF();
  
            int n= Integer.parseInt(seuilVarTFIDF.getText());

            String choixIndex = (String) jComboBox_ChoixIndex.getSelectedItem();


            if((n!=0)&&(n < 100000)&&(!seuilVarTFIDF.getText().equals(" "))){

                switch (choixIndex) {
                    case "Type Index":
                        JOptionPane.showMessageDialog(this,"Veuillez choisir le type de l'index .","Type de l'index non choisi ", JOptionPane.WARNING_MESSAGE);
                        break;
                    case "Primitif":
                        try {
                            
                            //new  IndexClass().indexCentral();
                            
                            cltfidf.fichierArffSeuilTFIDFIndexCentral();

                        } catch (ClassNotFoundException | SQLException | IOException ex) {
                            Logger.getLogger(VueProjet.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    case "Sans Stop Words":
                        try {
                            
                            new  IndexClass().indexStopWordsElimination();
                            
                            cltfidf.fichierArffSeuilTFIDFIndexStopWords();

                        } catch (ClassNotFoundException | SQLException | IOException ex) {
                            Logger.getLogger(VueProjet.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    case "Sans Stop Words et seuil":
                        try {
                            
                            new  IndexClass().indexStopWordsEliminationSeuilMot();
                            
                            cltfidf.fichierArffSeuilTFIDFIndexStopWordsSeuilMot();

                        } catch (ClassNotFoundException | SQLException | IOException ex) {
                            Logger.getLogger(VueProjet.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    case "Semantique":
                        try {
                            
                            new  IndexClass().indexSemantique();
                            
                            cltfidf.fichierArffSeuilTFIDFIndexSemantique();

                        } catch (ClassNotFoundException | SQLException | IOException ex) {
                            Logger.getLogger(VueProjet.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;   
                    case "Morphosyntaxique":
                        try {
                            
                            new  IndexClass().indexFiltreMorphSyntax();
                            
                            cltfidf.fichierArffSeuilTFIDFIndexMorphoSyntax();

                        } catch (ClassNotFoundException | SQLException | IOException ex) {
                            Logger.getLogger(VueProjet.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    case "Hybride":
                        try {
                            
                            new  IndexClass().indexFiltreCombine();
                            
                            cltfidf.fichierArffSeuilTFIDFIndexCombine();

                        } catch (ClassNotFoundException | SQLException | IOException ex) {
                            Logger.getLogger(VueProjet.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    default:
                        break;
                }
            }else {
                 JOptionPane.showMessageDialog(this,"Le seuil est compris entre 1 et 10000.","Erreur seuil", JOptionPane.ERROR_MESSAGE);

            }
    }//GEN-LAST:event_BouttonFichierArffActionPerformed

